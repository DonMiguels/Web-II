BEGIN;

-- ------------------------------------------------------------
-- Dominios de validacion institucional
-- ------------------------------------------------------------
CREATE DOMAIN email_address AS VARCHAR(150)
    CHECK (VALUE ~* '^[A-Z0-9._%+\-]+@[A-Z0-9.\-]+\.[A-Z]{2,}$');

CREATE DOMAIN document_identifier AS VARCHAR(30)
    CHECK (VALUE ~ '^[A-Za-z0-9\-]{5,30}$');

CREATE DOMAIN phone_number AS VARCHAR(30)
    CHECK (VALUE ~ '^\+?[0-9 ()\-]{7,20}$');

-- ------------------------------------------------------------
-- Convencion de metacampos compartidos
-- name, description, is_active, created_at, updated_at
-- Se aplica a catalogos y entidades maestras que lo ameritan.
-- ------------------------------------------------------------

CREATE TABLE IF NOT EXISTS category_type (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    description TEXT,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_category_type_name UNIQUE (name),
    CONSTRAINT ck_category_type_name_not_blank CHECK (btrim(name) <> '')
);

CREATE TABLE IF NOT EXISTS category (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    description TEXT,
    type_id BIGINT NOT NULL REFERENCES category_type(id) ON DELETE RESTRICT, -- evita borrado accidental del tipo en uso
    CONSTRAINT uq_category_name_type UNIQUE (name, type_id),
    CONSTRAINT ck_category_name_not_blank CHECK (btrim(name) <> '')
);

CREATE TABLE IF NOT EXISTS feature (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    description TEXT,
    CONSTRAINT uq_feature_name UNIQUE (name),
    CONSTRAINT ck_feature_name_not_blank CHECK (btrim(name) <> '')
);

CREATE TABLE IF NOT EXISTS item (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code VARCHAR(50) NOT NULL,
    name VARCHAR(120) NOT NULL,
    description TEXT,
    cost NUMERIC(12,2) NOT NULL DEFAULT 0,
    acquisition_date DATE,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    category_id BIGINT NOT NULL REFERENCES category(id) ON DELETE RESTRICT, -- evita eliminar categoria con inventario historico
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    deleted_at TIMESTAMPTZ,
    CONSTRAINT ck_item_code_not_blank CHECK (btrim(code) <> ''),
    CONSTRAINT ck_item_name_not_blank CHECK (btrim(name) <> ''),
    CONSTRAINT ck_item_cost_non_negative CHECK (cost >= 0)
);

CREATE TABLE IF NOT EXISTS item_feature (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    value TEXT,
    feature_id BIGINT NOT NULL REFERENCES feature(id) ON DELETE RESTRICT, -- conserva catalogo de caracteristicas en uso
    item_id BIGINT NOT NULL REFERENCES item(id) ON DELETE RESTRICT, -- se evita cascada por trazabilidad historica
    CONSTRAINT uq_item_feature_pair UNIQUE (feature_id, item_id)
);

CREATE TABLE IF NOT EXISTS location_type (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    description TEXT,
    CONSTRAINT uq_location_type_name UNIQUE (name),
    CONSTRAINT ck_location_type_name_not_blank CHECK (btrim(name) <> '')
);

CREATE TABLE IF NOT EXISTS location (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(120) NOT NULL,
    description TEXT,
    parent_id BIGINT REFERENCES location(id) ON DELETE SET NULL, -- al borrar padre, el hijo mantiene trazabilidad sin romper FK
    type_id BIGINT REFERENCES location_type(id) ON DELETE SET NULL, -- se conserva ubicacion aunque el tipo sea descontinuado
    CONSTRAINT ck_location_name_not_blank CHECK (btrim(name) <> '')
);

CREATE TABLE IF NOT EXISTS inventory (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    amount INTEGER NOT NULL DEFAULT 0,
    location_id BIGINT NOT NULL REFERENCES location(id) ON DELETE RESTRICT, -- evita eliminar ubicacion con stock
    item_id BIGINT NOT NULL REFERENCES item(id) ON DELETE RESTRICT, -- evita eliminar item con stock
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_inventory_location_item UNIQUE (location_id, item_id),
    CONSTRAINT ck_inventory_amount_non_negative CHECK (amount >= 0)
);

CREATE TABLE IF NOT EXISTS period_type (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    description TEXT,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT uq_period_type_name UNIQUE (name),
    CONSTRAINT ck_period_type_name_not_blank CHECK (btrim(name) <> '')
);

CREATE TABLE IF NOT EXISTS period (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    description TEXT,
    start_date DATE,
    end_date DATE,
    type_id BIGINT NOT NULL REFERENCES period_type(id) ON DELETE RESTRICT, -- evita eliminar tipo de periodo utilizado
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT uq_period_name_type UNIQUE (name, type_id),
    CONSTRAINT ck_period_name_not_blank CHECK (btrim(name) <> ''),
    CONSTRAINT ck_period_date_range CHECK (
        end_date IS NULL OR start_date IS NULL OR end_date >= start_date
    )
);

CREATE TABLE IF NOT EXISTS movement_type (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    description TEXT,
    CONSTRAINT uq_movement_type_name UNIQUE (name),
    CONSTRAINT ck_movement_type_name_not_blank CHECK (btrim(name) <> '')
);

CREATE TABLE IF NOT EXISTS person (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    document_id document_identifier NOT NULL,
    first_name VARCHAR(100) NOT NULL,
    last_name VARCHAR(100) NOT NULL,
    phone phone_number,
    degree VARCHAR(150),
    address TEXT,
    birth_date DATE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    deleted_at TIMESTAMPTZ,
    CONSTRAINT ck_person_first_name_not_blank CHECK (btrim(first_name) <> ''),
    CONSTRAINT ck_person_last_name_not_blank CHECK (btrim(last_name) <> '')
);

CREATE TABLE IF NOT EXISTS person_group (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    person_id BIGINT NOT NULL REFERENCES person(id) ON DELETE RESTRICT, -- se bloquea borrado de persona con asignaciones
    CONSTRAINT uq_person_group_person UNIQUE (person_id)
);

-- Definicion de la tabla "user"
CREATE TABLE IF NOT EXISTS "user" (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(60) NOT NULL,
    email email_address,
    password_hash TEXT NOT NULL,
    is_solvency BOOLEAN NOT NULL DEFAULT TRUE,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    person_id BIGINT REFERENCES person(id) ON DELETE SET NULL, -- se conserva usuario aunque persona asociada se desactive
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    deleted_at TIMESTAMPTZ,
    CONSTRAINT ck_user_name_not_blank CHECK (btrim(name) <> ''),
    CONSTRAINT ck_user_password_hash_not_blank CHECK (btrim(password_hash) <> '')
);

CREATE TABLE IF NOT EXISTS movement (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    booking_date TIMESTAMPTZ,
    estimated_return_date TIMESTAMPTZ,
    actual_return_date TIMESTAMPTZ,
    observations TEXT,
    user_id BIGINT NOT NULL REFERENCES "user"(id) ON DELETE RESTRICT, -- evita borrar usuario con movimientos historicos
    type_id BIGINT NOT NULL REFERENCES movement_type(id) ON DELETE RESTRICT, -- mantiene consistencia del tipo
    period_id BIGINT NOT NULL REFERENCES period(id) ON DELETE RESTRICT, -- mantiene consistencia temporal academica
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT ck_movement_date_order CHECK (
        actual_return_date IS NULL OR booking_date IS NULL OR actual_return_date >= booking_date
    )
);

CREATE TABLE IF NOT EXISTS movement_detail (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    movement_date TIMESTAMPTZ,
    amount INTEGER NOT NULL DEFAULT 1,
    fine NUMERIC(12,2) NOT NULL DEFAULT 0,
    observations TEXT,
    inventory_id BIGINT NOT NULL REFERENCES inventory(id) ON DELETE RESTRICT, -- evita borrar inventario con historial transaccional
    movement_id BIGINT NOT NULL REFERENCES movement(id) ON DELETE RESTRICT, -- evita cascada accidental en historial
    CONSTRAINT ck_movement_detail_amount_positive CHECK (amount > 0),
    CONSTRAINT ck_movement_detail_fine_non_negative CHECK (fine >= 0)
);

CREATE TABLE IF NOT EXISTS return_status_type (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    description TEXT,
    CONSTRAINT uq_return_status_type_name UNIQUE (name),
    CONSTRAINT ck_return_status_type_name_not_blank CHECK (btrim(name) <> '')
);

CREATE TABLE IF NOT EXISTS return_status (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    type_id BIGINT NOT NULL REFERENCES return_status_type(id) ON DELETE RESTRICT, -- catalogo controlado de estados
    status_date TIMESTAMPTZ,
    observations TEXT,
    movement_detail_id BIGINT NOT NULL REFERENCES movement_detail(id) ON DELETE RESTRICT, -- conserva historial de devolucion
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS audit_type (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    description TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_audit_type_name UNIQUE (name),
    CONSTRAINT ck_audit_type_name_not_blank CHECK (btrim(name) <> '')
);

CREATE TABLE IF NOT EXISTS audit (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    entity_name VARCHAR(100) NOT NULL,
    method VARCHAR(50) NOT NULL,
    event_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    details TEXT,
    user_id BIGINT NOT NULL REFERENCES "user"(id) ON DELETE RESTRICT, -- evita borrar usuario con auditoria
    type_id BIGINT NOT NULL REFERENCES audit_type(id) ON DELETE RESTRICT, -- mantiene catalogo historico
    CONSTRAINT ck_audit_entity_name_not_blank CHECK (btrim(entity_name) <> ''),
    CONSTRAINT ck_audit_method_not_blank CHECK (btrim(method) <> '')
);

CREATE TABLE IF NOT EXISTS notification_type (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    description TEXT,
    CONSTRAINT uq_notification_type_name UNIQUE (name),
    CONSTRAINT ck_notification_type_name_not_blank CHECK (btrim(name) <> '')
);

CREATE TABLE IF NOT EXISTS notification (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR(150) NOT NULL,
    message TEXT NOT NULL,
    sent_at TIMESTAMPTZ,
    is_read BOOLEAN NOT NULL DEFAULT FALSE,
    user_id BIGINT NOT NULL REFERENCES "user"(id) ON DELETE RESTRICT, -- evita borrado de receptor con historial
    type_id BIGINT NOT NULL REFERENCES notification_type(id) ON DELETE RESTRICT, -- mantiene tipologia estable
    CONSTRAINT ck_notification_title_not_blank CHECK (btrim(title) <> ''),
    CONSTRAINT ck_notification_message_not_blank CHECK (btrim(message) <> '')
);

CREATE TABLE IF NOT EXISTS profile (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    description TEXT,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT ck_profile_name_not_blank CHECK (btrim(name) <> '')
);

CREATE TABLE IF NOT EXISTS user_profile (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL REFERENCES "user"(id) ON DELETE RESTRICT, -- evita eliminar usuarios con perfiles asignados
    profile_id BIGINT NOT NULL REFERENCES profile(id) ON DELETE RESTRICT, -- evita eliminar perfiles en uso
    CONSTRAINT uq_user_profile UNIQUE (user_id, profile_id)
);

-- Definicion de la tabla "option"
CREATE TABLE IF NOT EXISTS "option" (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    description TEXT,
    CONSTRAINT uq_option_name UNIQUE (name),
    CONSTRAINT ck_option_name_not_blank CHECK (btrim(name) <> '')
);

CREATE TABLE IF NOT EXISTS option_profile (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    profile_id BIGINT NOT NULL REFERENCES profile(id) ON DELETE RESTRICT, -- seguridad: no borrar perfil en uso
    option_id BIGINT NOT NULL REFERENCES "option"(id) ON DELETE RESTRICT, -- seguridad: no borrar opcion asignada
    CONSTRAINT uq_option_profile UNIQUE (profile_id, option_id)
);

CREATE TABLE IF NOT EXISTS subsystem (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    description TEXT,
    CONSTRAINT uq_subsystem_name UNIQUE (name),
    CONSTRAINT ck_subsystem_name_not_blank CHECK (btrim(name) <> '')
);

CREATE TABLE IF NOT EXISTS menu (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    description TEXT,
    subsystem_id BIGINT NOT NULL REFERENCES subsystem(id) ON DELETE RESTRICT, -- evita cascada en estructura de navegacion
    CONSTRAINT uq_menu_name_subsystem UNIQUE (name, subsystem_id),
    CONSTRAINT ck_menu_name_not_blank CHECK (btrim(name) <> '')
);

CREATE TABLE IF NOT EXISTS option_menu (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    option_id BIGINT NOT NULL REFERENCES "option"(id) ON DELETE RESTRICT, -- mantiene mapa de navegacion estable
    menu_id BIGINT NOT NULL REFERENCES menu(id) ON DELETE RESTRICT, -- mantiene menu referenciado
    CONSTRAINT uq_option_menu UNIQUE (option_id, menu_id)
);

-- Definicion de la tabla "class"
CREATE TABLE IF NOT EXISTS "class" (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    description TEXT,
    CONSTRAINT uq_class_name UNIQUE (name),
    CONSTRAINT ck_class_name_not_blank CHECK (btrim(name) <> '')
);

CREATE TABLE IF NOT EXISTS subsystem_class (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    subsystem_id BIGINT NOT NULL REFERENCES subsystem(id) ON DELETE RESTRICT, -- evita borrado de subsistema en uso
    class_id BIGINT NOT NULL REFERENCES "class"(id) ON DELETE RESTRICT, -- evita borrado de clase en uso
    CONSTRAINT uq_subsystem_class UNIQUE (subsystem_id, class_id)
);

CREATE TABLE IF NOT EXISTS "method" (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    description TEXT,
    CONSTRAINT uq_method_name UNIQUE (name),
    CONSTRAINT ck_method_name_not_blank CHECK (btrim(name) <> '')
);

CREATE TABLE IF NOT EXISTS class_method (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    class_id BIGINT NOT NULL REFERENCES "class"(id) ON DELETE RESTRICT, -- evita borrado de clase con metodos asociados
    method_id BIGINT NOT NULL REFERENCES "method"(id) ON DELETE RESTRICT, -- evita borrado de metodo con dependencias
    CONSTRAINT uq_class_method UNIQUE (class_id, method_id)
);

CREATE TABLE IF NOT EXISTS method_profile (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    method_id BIGINT NOT NULL REFERENCES "method"(id) ON DELETE RESTRICT, -- evita perdida de matriz de autorizacion
    profile_id BIGINT NOT NULL REFERENCES profile(id) ON DELETE RESTRICT, -- evita perdida de matriz de autorizacion
    CONSTRAINT uq_method_profile UNIQUE (method_id, profile_id)
);

-- Definicion de la tabla "transaction"
CREATE TABLE IF NOT EXISTS "transaction" (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    subsystem_id BIGINT NOT NULL REFERENCES subsystem(id) ON DELETE RESTRICT, -- seguridad: no borrar subsistema con transacciones
    class_id BIGINT NOT NULL REFERENCES "class"(id) ON DELETE RESTRICT, -- seguridad: no borrar clase con transacciones
    method_id BIGINT NOT NULL REFERENCES "method"(id) ON DELETE RESTRICT, -- seguridad: no borrar metodo con transacciones
    CONSTRAINT uq_transaction_scope UNIQUE (subsystem_id, class_id, method_id)
);

